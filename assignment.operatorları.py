#dəyişən təyin edirik
a = 5
print(a)  # nəticə: 5

# bərabərdir təyinat operatoru (=)
a = 10
print(a)  # nəticə: 10

# artırma təyinat operatoru (+=)
a += 3
print(a)  # nəticə: 13

# çıxarma təyinat operatoru (-=)
a -= 5
print(a)  # nəticə: 8

# vurma təyinat operatoru (*=)
a *= 2
print(a)  # nəticə: 16

# bölmə təyinat operatoru (/=)
a /= 4
print(a)  # nəticə: 4.0

# tam bölmə təyinat operatoru (//=)
a //= 2
print(a)  # nəticə: 2.0

# qalıq tapmaq təyinat operatoru (%=)
a %= 1
print(a)  # nəticə: 0.0

# üstə yüksəltmə təyinat operatoru (**=)
a **= 3
print(a)  # nəticə: 0.0
