#integer dəyişənlərini təyin edirik
a = 10
b = 3

#Toplanması(+)
c = a + b
print(c)  # nəticə: 13

#Çıxılması (-)
d = a - b
print(d)  # nəticə: 7

#Vurulması (*)
e = a * b
print(e)  # nəticə: 30

#Bölünməsi (/)
f = a / b
print(f)  # nəticə: 3.3333333333333335

#Tam bölünməsi (//)
g = a // b
print(g)  # nəticə: 3

#Qalıq tapılması (%)
h = a % b
print(h)  # nəticə: 1

#Üstə yüksəldilməsi (**)
i = a ** b
print(i)  # nəticə: 1000
