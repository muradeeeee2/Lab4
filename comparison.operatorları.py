x = 5
y = 3

# == operatoru, iki dəyişənin dəyərlərinin bərabərliyini yoxlayır və nəticə kimi doğru və ya yanlış dəyər verir.
print(x == y)  # nəticə: False
print(x == 5)  # nəticə: True

# != operatoru, iki dəyişənin dəyərlərinin bərabər olmamasını yoxlayır və nəticə kimi doğru və ya yanlış dəyər verir.
print(x != y)  # nəticə: True
print(x != 5)  # nəticə: False

# > operatoru, sol tərəfdəki dəyişənin dəyərinin sağ tərəfdəki dəyişənə nisbətən böyük olub olmadığını yoxlayır 
# və nəticə kimi doğru və ya yanlış dəyər verir.
print(x > y)   # nəticə: True
print(x > 5)   # nəticə: False

# < operatoru, sol tərəfdəki dəyişənin dəyərinin sağ tərəfdəki dəyişənə nisbətən kiçik olub olmadığını yoxlayır 
# və nəticə kimi doğru və ya yanlış dəyər verir.
print(x < y)   # nəticə: False
print(x < 5)   # nəticə: False

# >= operatoru, sol tərəfdəki dəyişənin dəyərinin sağ tərəfdəki dəyişəndən böyük və ya bərabər olub olmadığını yoxlayır 
# və nəticə kimi doğru və ya yanlış dəyər verir.
print(x >= y)  # nəticə: True
print(x >= 5)  # nəticə: True

# <= operatoru, sol tərəfdəki dəyişənin dəyərinin sağ tərəfdəki dəyişəndən kiçik və ya bərabər olub olmadığını yoxlayır 
# və nəticə kimi doğru və ya yanlış dəyər verir.
print(x <= y)  # nəticə: False
print(x <= 5)  # nəticə: True
